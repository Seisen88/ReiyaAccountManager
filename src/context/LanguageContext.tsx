export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLang] = useState(() => localStorage.getItem("reiya_language") || "en");

  const t = (key: string): string => {
    const langDict = translations[language] || translations["en"];
    return langDict[key] || translations["en"][key] || key;
  };

  const setLanguage = (lang: string) => {
    setLang(lang);
    localStorage.setItem("reiya_language", lang);
    window.dispatchEvent(new Event("reiya-language-changed"));
  };

  useEffect(() => {
    const handleStorageChange = () => {
      const current = localStorage.getItem("reiya_language") || "en";
      if (current !== language) {
        setLang(current);
      }
    };
    window.addEventListener("storage", handleStorageChange);
    window.addEventListener("reiya-language-changed", handleStorageChange);
    return () => {
      window.removeEventListener("storage", handleStorageChange);
      window.removeEventListener("reiya-language-changed", handleStorageChange);
    };
  }, [language]);

  return (
    <LanguageContext.Provider value={{ language, t, setLanguage }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
};
